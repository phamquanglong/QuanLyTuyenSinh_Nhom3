public class Colors {
    public Color backColor = Color.white;
    public Color primaryColor = Color.decode("#00CCFF");
    public Color danger = Color.RED;
    public Color warning = Color.ORANGE;

    public Color getBackColor() {
        return backColor;
    }

    public Color getPrimaryColor() {
        return primaryColor;
    }

    public Color getDanger() {
        return danger;
    }

    public Color getWarning() {
        return warning;
    }
    
    
}
